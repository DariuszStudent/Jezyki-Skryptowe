import math

numer = 45.12345676
print(math.trunc(numer))  # ucięcie
print(math.ceil(numer))  # do sufitu
print(math.floor(numer))  # do podłogi
print(round(numer, 3))  # zaokrąglenie
print("Liczba: {0:.3f}".format(numer))

